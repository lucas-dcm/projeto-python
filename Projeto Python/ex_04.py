'''Calculo da média de 4 notas bimestrais'''

nota1 = input ('Digite sua nota 1: ')
nota2 = input ('Digite sua nota 2: ')
nota3 = input ('Digite sua nota 3: ')
nota4 = input ('Digite sua nota 4: ')

soma = int(nota1) + int(nota2) + int(nota3) + int(nota4)

media = float(soma) / 4

print ('A media das notas é', media)